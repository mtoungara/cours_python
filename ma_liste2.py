ma_liste = [1,2,"Amine","Martin",-5,False]
print(ma_liste[3])  # Affiche "Martin"
print(ma_liste[-1]) # Affiche False
print(ma_liste[0:3]) # Affiche [1, 2, "Amine"]
print(ma_liste[2:])  # Affiche ["Amine", "Martin", -5, False]
print(ma_liste[:4])  # Affiche [1, 2, "Amine", "Martin"]
print(ma_liste[:])   # Affiche [1, 2, "Amine    ", "Martin", -5, False]
print(ma_liste[1:5:2]) # Affiche [2, "Martin"]
print(ma_liste[::-1])  # Affiche [False, -5, "Martin", "Amine", 2, 1]

